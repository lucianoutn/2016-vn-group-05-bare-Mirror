
    create table Bancos (
        nombre varchar(255),
        id_Poi bigint not null,
        primary key (id_Poi)
    )

    create table Busqueda (
        id_Busqueda integer generated by default as identity (start with 1),
        cantResultados integer not null,
        fecha varbinary(255),
        fraseBuscada varchar(255),
        terminal varchar(255),
        tiempoBusqueda integer not null,
        usuario_id integer,
        primary key (id_Busqueda)
    )

    create table Busqueda_POIs (
        id_Busqueda integer not null,
        id_Poi bigint not null
    )

    create table BusquedasPorFecha (
        id bigint not null,
        primary key (id)
    )

    create table CGPES (
        id_Poi bigint not null,
        comuna_nroComuna integer,
        primary key (id_Poi)
    )

    create table Comuna (
        nroComuna integer not null,
        primary key (nroComuna)
    )

    create table DiaAtencion (
        id_diaAtencion bigint generated by default as identity (start with 1),
        dia varchar(255),
        horaApertura integer not null,
        horaCierre integer not null,
        primary key (id_diaAtencion)
