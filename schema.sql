    create table Paradas (
        numeroDeLinea varchar(255),
        id_Poi bigint not null,
        primary key (id_Poi)
    )

    create table Pois (
        id_Poi bigint generated by default as identity (start with 1),
        altura varchar(255),
        calle varchar(255),
        coordenadaX double not null,
        coordenadaY double not null,
        toleranciaEnCuadras integer not null,
        posicion_id_Punto integer,
        primary key (id_Poi)
    )

    create table Punto (
        id_Punto integer generated by default as identity (start with 1),
        x double not null,
        y double not null,
        id integer,
        nro_puntos integer,
        primary key (id_Punto)
    )

    create table ReportePorBusqueda (
        id bigint generated by default as identity (start with 1),
        cantidadDeResultados double not null,
        demoraDeBusqueda double not null,
        fraseBuscada varchar(255),
        numeroDeRenglon bigint,
        primary key (id)
    )

    create table ReportePorFecha (
        id bigint generated by default as identity (start with 1),
        cantidadDeBusquedas double not null,
        diaDeLaBusqueda timestamp,
        numDeRenglon bigint,
        primary key (id)
    )

    create table ReportePorTerminal (
        id bigint generated by default as identity (start with 1),
        unaTerminal varchar(255),
        numeroDeRenglon bigint,
        primary key (id)
    )

    create table ResultadosDeBusquedas (
        id bigint not null,
        primary key (id)
    )

    create table ResultadosPorTerminal (
        id bigint not null,
        primary key (id)
    )

    create table Servicio (
        id_Servicio bigint generated by default as identity (start with 1),
        nombre varchar(255),
        nroServicio bigint,
        primary key (id_Servicio)
    )

    create table T_RESULTADO_BUSQUEDA (
        CD_BUSQUEDA integer not null,
        QT_RESULTADOS integer,
        CD_TERMINAL integer,
        DC_FRASE_BUSCADA varchar(255),
        TIEMPO_BUSQUEDA integer,
        primary key (CD_BUSQUEDA)
    )

    create table Usuario (
        id integer generated by default as identity (start with 1),
        nombre varchar(255),
        comuna_nroComuna integer,
        primary key (id)
    )

    create table cantidadDeResultados (
        resultado_id bigint not null,
        cantResultados double
    )

